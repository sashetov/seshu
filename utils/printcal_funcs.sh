function tableify_weeks_cal(){
  export COLNAMES='YEAR,MNAM,WNUM, ,MO,TU,WE,TH,FR,SA,SU'
  { while read line; do echo $line; done; } | \
  column \
    -t \
    -c 0\
    -s ','\
    -o ' '\
    --table-columns "$COLNAMES"\
    --table-right "$COLNAMES";
}
